package de.elnarion.sample.domain;

/**
 * The Interface DomainInterface.
 */
public interface DomainInterface {

	/**
	 * Some method.
	 */
	void someMethod();
	
	/**
	 * Some method with parameter.
	 *
	 * @param paramParameter the param parameter
	 */
	void someMethodWithParameter(String paramParameter);
	
	/**
	 * Some method with return value.
	 *
	 * @return the string
	 */
	String someMethodWithReturnValue();
}
